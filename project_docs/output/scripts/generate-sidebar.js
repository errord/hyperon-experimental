import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..", "docs");
const OUT = path.join(ROOT, ".vitepress", "sidebar.generated.ts");

const SKIP = new Set([".vitepress", "public", "node_modules"]);

function humanize(name) {
  return name
    .split("-")
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

function readTitle(absMd) {
  try {
    const s = fs.readFileSync(absMd, "utf8");
    const fmEnd = s.startsWith("---") ? s.indexOf("\n---", 3) : -1;
    const body = fmEnd >= 0 ? s.slice(fmEnd + 4) : s;
    const m = body.match(/^\s*#\s+(.+)$/m);
    if (m) return m[1].trim().replace(/\s+#+\s*$/, "");
  } catch {
    /* ignore */
  }
  return humanize(path.basename(absMd, ".md"));
}

function linkPath(routePrefix, fileName) {
  const stem = fileName.replace(/\.md$/, "");
  const p = routePrefix ? `${routePrefix}/${stem}` : stem;
  if (stem === "index") {
    const dir = routePrefix;
    return dir ? `/${dir}/` : "/";
  }
  return `/${p}`;
}

function collect(absDir, routePrefix) {
  const items = [];
  let dirents;
  try {
    dirents = fs.readdirSync(absDir, { withFileTypes: true });
  } catch {
    return items;
  }

  const dirs = dirents
    .filter((d) => d.isDirectory() && !d.name.startsWith(".") && !SKIP.has(d.name))
    .sort((a, b) => a.name.localeCompare(b.name));

  const files = dirents
    .filter((d) => d.isFile() && d.name.endsWith(".md"))
    .sort((a, b) => a.name.localeCompare(b.name));

  for (const d of dirs) {
    const nextRoute = routePrefix ? `${routePrefix}/${d.name}` : d.name;
    const sub = collect(path.join(absDir, d.name), nextRoute);
    if (sub.length) {
      items.push({
        text: humanize(d.name),
        collapsed: true,
        items: sub,
      });
    }
  }

  for (const f of files) {
    const link = linkPath(routePrefix, f.name);
    if (link === "/") continue;
    items.push({
      text: readTitle(path.join(absDir, f.name)),
      link,
    });
  }

  return items;
}

function hasMarkdownUnder(absDir) {
  let dirents;
  try {
    dirents = fs.readdirSync(absDir, { withFileTypes: true });
  } catch {
    return false;
  }
  for (const d of dirents) {
    if (d.isFile() && d.name.endsWith(".md")) return true;
    if (d.isDirectory() && !d.name.startsWith(".") && !SKIP.has(d.name)) {
      if (hasMarkdownUnder(path.join(absDir, d.name))) return true;
    }
  }
  return false;
}

function serializeItem(item, indent) {
  const pad = " ".repeat(indent);
  if (item.items) {
    const inner = item.items.map((x) => serializeItem(x, indent + 2)).join(",\n");
    return `${pad}{\n${pad}  text: ${JSON.stringify(item.text)},\n${pad}  collapsed: ${item.collapsed ?? true},\n${pad}  items: [\n${inner}\n${pad}  ]\n${pad}}`;
  }
  return `${pad}{\n${pad}  text: ${JSON.stringify(item.text)},\n${pad}  link: ${JSON.stringify(item.link)}\n${pad}}`;
}

function topLevelDirs() {
  return fs
    .readdirSync(ROOT, { withFileTypes: true })
    .filter((d) => d.isDirectory() && !d.name.startsWith(".") && !SKIP.has(d.name))
    .map((d) => d.name)
    .sort((a, b) => a.localeCompare(b));
}

const sidebarMulti = {};
for (const dir of topLevelDirs()) {
  const abs = path.join(ROOT, dir);
  if (!hasMarkdownUnder(abs)) continue;
  const items = collect(abs, dir);
  if (items.length) {
    sidebarMulti[`/${dir}/`] = items;
  }
}

const keys = Object.keys(sidebarMulti).sort((a, b) => a.localeCompare(b));
const blocks = keys.map((k) => {
  const arr = sidebarMulti[k];
  const inner = arr.map((x) => serializeItem(x, 4)).join(",\n");
  return `  ${JSON.stringify(k)}: [\n${inner}\n  ]`;
});

const file = `/* eslint-disable */\n/* Auto-generated by scripts/generate-sidebar.js — do not edit by hand */\n\nimport type { DefaultTheme } from 'vitepress'\n\nexport const sidebarMulti: DefaultTheme.SidebarMulti = {\n${blocks.join(",\n")}\n}\n`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, file, "utf8");
console.log("Wrote", OUT);